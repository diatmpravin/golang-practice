package main

import "fmt"

func main() {
	var input int = 5

	if input == 0 {
		fmt.Println("Zero")
	} else if input == 1 {
		fmt.Println("One")
	} else if input == 2 {
		fmt.Println("Two")
	} else if input == 3 {
		fmt.Println("Three")
	} else if input == 4 {
		fmt.Println("Four")
	} else if input == 5 {
		fmt.Println("Five")
	}

}
